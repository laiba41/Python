if a == 240:
    print(bool(a))
else:
    print(a)


if a != 40:
    print(bool(a))
else:
    print(a)


if a > 44:
    print(bool(a))
else:
    print(a)


if a >= 33:
    print(bool(a))
else:
    print(a)


if a < 20:
    print(bool(a))
else:
    print(a)


